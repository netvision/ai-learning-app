// Helper function to get topics by grade and subject
export const getTopicsByGradeAndSubject = (grade, subject) => {
  if (!syllabus[grade]) return [];
  return syllabus[grade][subject] || [];
};

// Helper function to get all subjects for a grade
export const getSubjectsByGrade = (grade) => {
  if (!syllabus[grade]) return [];
  return Object.keys(syllabus[grade]);
};

// Helper function to get all grades
export const getAllGrades = () => {
  return Object.keys(syllabus);
};
